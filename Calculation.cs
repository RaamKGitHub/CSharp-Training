using System;
namespace CalculationLogic
{

public class Calculation
{
		public static int Add(int a, int b)
		{
				  int addRes=a+b;
				return addRes;
		}
		
		public static int Sub(int m, int n)
		{
				 int subRes=m-n;
				return subRes;
		}
				public static int Mul(int j, int k)
		{
				 int mulRes=j*k;
				return mulRes;
		}
		
}
}
